function initAssociationsCarousel(){
  const section = document.getElementById('associations');
  if(!section) return;

  const track = section.querySelector('.carousel__track');
  const slides = Array.from(section.querySelectorAll('.carousel__slide'));
  const prevBtn = section.querySelector('.carousel__arrow--prev');
  const nextBtn = section.querySelector('.carousel__arrow--next');
  const dotsContainer = section.querySelector('.carousel__dots');
  if(!track || slides.length === 0) return;

  let index = 0;

  function slidesPerView(){
    const width = window.innerWidth;
    if(width <= 520) return 2;    // small mobile -> 2
    if(width <= 768) return 2;    // tablet -> 2
    if(width <= 1024) return 3;   // laptop -> 3
    return 3;                      // desktop -> 3
  }

  function update(){
    const perView = slidesPerView();
    const maxIndex = Math.max(0, slides.length - perView);
    if(index < 0) index = 0;
    if(index > maxIndex) index = maxIndex;
    // Align by the target slide's left offset (robust against rounding/gaps)
    const targetSlide = slides[index];
    const offset = -targetSlide.offsetLeft;
    track.style.transform = `translateX(${offset}px)`;
    updateButtons(perView);
    updateDots(perView);
  }

  function updateButtons(perView){
    const maxIndex = Math.max(0, slides.length - perView);
    if(prevBtn) prevBtn.disabled = index <= 0;
    if(nextBtn) nextBtn.disabled = index >= maxIndex;
  }

  function createDots(){
    dotsContainer.innerHTML = '';
    const perView = slidesPerView();
    const pages = Math.max(1, slides.length - perView + 1);
    for(let i=0; i<pages; i++){
      const dot = document.createElement('button');
      dot.className = 'carousel__dot';
      dot.type = 'button';
      dot.setAttribute('aria-label', `Go to slide ${i+1}`);
      dot.addEventListener('click', () => { index = i; update(); });
      dotsContainer.appendChild(dot);
    }
  }

  function updateDots(perView){
    const dots = Array.from(dotsContainer.querySelectorAll('.carousel__dot'));
    const pages = Math.max(1, slides.length - perView + 1);
    if(dots.length !== pages){ createDots(); }
    dots.forEach((d, i) => d.classList.toggle('is-active', i === index));
  }

  function next(){ index++; update(); }
  function prev(){ index--; update(); }

  nextBtn?.addEventListener('click', next);
  prevBtn?.addEventListener('click', prev);

  // Swipe support
  let startX = 0;
  let isDragging = false;
  let currentTranslate = 0;

  const viewport = section.querySelector('.carousel');
  if(viewport){
    const onTouchStart = (e) => { isDragging = true; startX = e.touches ? e.touches[0].clientX : e.clientX; track.style.transition = 'none'; };
    const onTouchMove = (e) => {
      if(!isDragging) return;
      const x = e.touches ? e.touches[0].clientX : e.clientX;
      const dx = x - startX;
      track.style.transform = `translateX(${currentTranslate + dx}px)`;
    };
    const onTouchEnd = (e) => {
      if(!isDragging) return;
      isDragging = false;
      track.style.transition = 'transform 350ms ease';
      const x = e.changedTouches ? e.changedTouches[0].clientX : e.clientX;
      const dx = x - startX;
      const threshold = 50;
      if(dx < -threshold) { index++; }
      if(dx > threshold) { index--; }
      update();
      // Save computed translate for next drag start (cross-browser)
      const style = getComputedStyle(track).transform;
      if(style && style !== 'none'){
        // style is like "matrix(a, b, c, d, tx, ty)" or "matrix3d(...)"
        if(style.startsWith('matrix(')){
          const parts = style.replace('matrix(', '').replace(')', '').split(',').map(s => parseFloat(s.trim()));
          currentTranslate = parts.length >= 6 ? parts[4] : 0;
        } else if(style.startsWith('matrix3d(')){
          const parts = style.replace('matrix3d(', '').replace(')', '').split(',').map(s => parseFloat(s.trim()));
          currentTranslate = parts.length >= 16 ? parts[12] : 0;
        } else {
          currentTranslate = 0;
        }
      } else {
        currentTranslate = 0;
      }
    };
    viewport.addEventListener('mousedown', onTouchStart);
    viewport.addEventListener('mousemove', onTouchMove);
    document.addEventListener('mouseup', onTouchEnd);
    viewport.addEventListener('touchstart', onTouchStart, { passive: true });
    viewport.addEventListener('touchmove', onTouchMove, { passive: true });
    viewport.addEventListener('touchend', onTouchEnd);
  }

  window.addEventListener('resize', () => { index = 0; update(); });

  // Initial
  createDots();
  update();
}

// ==============================
// Handled Cases: expandable accordion
// ==============================
function initHandledCases(){
  const section = document.getElementById('handled-cases');
  if(!section) return;

  const caseItems = Array.from(section.querySelectorAll('.case-item'));
  
  caseItems.forEach(item => {
    const button = item.querySelector('.case-btn');
    const panel = item.querySelector('.case-panel');
    
    if(!button || !panel) return;
    
    button.addEventListener('click', () => {
      const isExpanded = button.getAttribute('aria-expanded') === 'true';
      
      // Close all other panels
      caseItems.forEach(otherItem => {
        if(otherItem !== item) {
          const otherButton = otherItem.querySelector('.case-btn');
          const otherPanel = otherItem.querySelector('.case-panel');
          if(otherButton && otherPanel) {
            otherButton.setAttribute('aria-expanded', 'false');
            otherPanel.setAttribute('aria-hidden', 'true');
            otherItem.classList.remove('expanded');
          }
        }
      });
      
      // Toggle current panel
      if(isExpanded) {
        button.setAttribute('aria-expanded', 'false');
        panel.setAttribute('aria-hidden', 'true');
        item.classList.remove('expanded');
      } else {
        button.setAttribute('aria-expanded', 'true');
        panel.setAttribute('aria-hidden', 'false');
        item.classList.add('expanded');
      }
    });
  });
}