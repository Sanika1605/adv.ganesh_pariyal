// Language management class
class LanguageManager {
  constructor() {
    this.currentLanguage = localStorage.getItem('selectedLanguage') || null;
    this.languageSelection = document.getElementById('language-selection');
    this.siteContent = document.getElementById('site-content');
    this.languageSwitcher = document.getElementById('language-switcher');
    this.languageDropdown = document.getElementById('language-dropdown');
    this.currentLanguageIndicator = document.getElementById('current-language');
    
    // Debug: log what we found
    console.log('LanguageManager constructor - Elements found:');
    console.log('- languageSelection:', this.languageSelection);
    console.log('- siteContent:', this.siteContent);
    console.log('- languageSwitcher:', this.languageSwitcher);
    console.log('- languageDropdown:', this.languageDropdown);
    console.log('- currentLanguageIndicator:', this.currentLanguageIndicator);
    
    // Bound helpers
    this.translateElement = this.translateElement.bind(this);
    this.applyTranslationsIn = this.applyTranslationsIn.bind(this);
    this.observeMutations = this.observeMutations.bind(this);
    
    this.init();
  }
  
  init() {
    // Always show language selection first
    this.showLanguageSelection();
    // Set indicator (defaults to EN if none)
    this.updateCurrentLanguageIndicator(this.currentLanguage || 'en');
    // Add event listeners
    this.addEventListeners();
    // Start observing for dynamically added translatable content
    this.observeMutations();
  }
  
  addEventListeners() {
    console.log('Adding event listeners...');
    
    // Language selection buttons
    const languageButtons = document.querySelectorAll('.language-btn');
    console.log('Found language buttons:', languageButtons.length);
    languageButtons.forEach(btn => {
      btn.addEventListener('click', (e) => {
        const lang = e.currentTarget.dataset.lang;
        this.selectLanguage(lang);
      });
    });
    
    // Language switcher dropdown
    if (this.languageSwitcher) {
      console.log('Language switcher found, adding click listener');
      this.languageSwitcher.addEventListener('click', (e) => {
        console.log('Language switcher clicked!');
        e.stopPropagation();
        this.toggleDropdown();
      });
    } else {
      console.log('Language switcher NOT found!');
    }
    
    // Language dropdown options
    if (this.languageDropdown) {
      console.log('Language dropdown found, adding option listeners');
      const dropdownOptions = this.languageDropdown.querySelectorAll('.language-dropdown__option');
      console.log('Found dropdown options:', dropdownOptions.length);
      dropdownOptions.forEach(option => {
        option.addEventListener('click', (e) => {
          console.log('Dropdown option clicked:', e.currentTarget.dataset.lang);
          const lang = e.currentTarget.dataset.lang;
          this.selectLanguageFromDropdown(lang);
        });
      });
    } else {
      console.log('Language dropdown NOT found!');
    }
    
    // Close dropdown when clicking outside
    document.addEventListener('click', (e) => {
      if (!this.languageSwitcher?.contains(e.target) && !this.languageDropdown?.contains(e.target)) {
        this.hideDropdown();
      }
    });
    
    // Close dropdown on escape key
    document.addEventListener('keydown', (e) => {
      if (e.key === 'Escape') {
        this.hideDropdown();
      }
    });
  }
  
  selectLanguage(lang) {
    // Add loading state
    this.siteContent?.classList.add('language-loading');
    
    // Store language preference
    localStorage.setItem('selectedLanguage', lang);
    this.currentLanguage = lang;
    
    // Hide language selection
    this.hideLanguageSelection();
    
    // Set language and update content
    setTimeout(() => {
      this.setLanguage(lang);
      this.siteContent?.classList.remove('language-loading');
      // Now that language is applied, show the disclaimer
      document.dispatchEvent(new CustomEvent('language-selected', { detail: { lang } }));
    }, 200);
  }
  
  setLanguage(lang) {
    console.log('Setting language to:', lang);
    
    // Update document language
    document.documentElement.lang = lang;
    document.documentElement.setAttribute('data-lang', lang);
    
    // Update current language indicator
    this.updateCurrentLanguageIndicator(lang);
    
    // Update all translatable elements (existing)
    this.applyTranslationsIn(document);
  }
  
  updateCurrentLanguageIndicator(lang) {
    if (this.currentLanguageIndicator) {
      const languageCodes = {
        'en': 'EN',
        'hi': 'HI',
        'mr': 'MR'
      };
      this.currentLanguageIndicator.textContent = languageCodes[lang] || 'EN';
    }
  }
  
  getTranslation(key, lang) {
    // Prefer flat key lookup (our translations use flat keys like "nav.home")
    const langDict = translations[lang] || {};
    if (typeof langDict[key] === 'string') {
      return langDict[key];
    }

    // Fallback: support nested objects if ever used
    const parts = key.split('.');
    let node = langDict;
    for (const part of parts) {
      if (node && typeof node === 'object' && part in node) {
        node = node[part];
      } else {
        return null;
      }
    }
    return typeof node === 'string' ? node : null;
  }
  
  // Translate a single element according to its data attributes
  translateElement(element, lang) {
    if (!element || !element.hasAttribute('data-i18n')) return;
    const key = element.getAttribute('data-i18n');
    const translation = this.getTranslation(key, lang);
    if (!translation) return;

    // Optional: specify where to apply the translation
    // data-i18n-target can be: text|html|placeholder|title|aria-label
    const target = element.getAttribute('data-i18n-target');

    if (target === 'text') {
      element.textContent = translation;
      return;
    }
    if (target === 'html') {
      element.innerHTML = translation;
      return;
    }
    if (target === 'placeholder') {
      element.placeholder = translation;
      return;
    }
    if (target === 'title') {
      element.setAttribute('title', translation);
      return;
    }
    if (target === 'aria-label') {
      element.setAttribute('aria-label', translation);
      return;
    }

    // Backward-compatible default behavior
    if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
      element.placeholder = translation;
    } else {
      element.innerHTML = translation;
    }
  }

  // Apply translations within a container (Node | Document)
  applyTranslationsIn(rootNode) {
    const lang = this.currentLanguage || 'en';
    const root = (rootNode && typeof rootNode.querySelectorAll === 'function') ? rootNode : document;
    // Translate root itself if it has data-i18n
    if (root !== document && root.hasAttribute && root.hasAttribute('data-i18n')) {
      this.translateElement(root, lang);
    }
    const elements = root.querySelectorAll('[data-i18n]');
    console.log('Applying translations in root. Count:', elements.length);
    elements.forEach(el => this.translateElement(el, lang));
  }

  // Observe DOM for dynamically inserted elements and translate them
  observeMutations() {
    if (!window.MutationObserver) return;
    const observer = new MutationObserver((mutations) => {
      const lang = this.currentLanguage || 'en';
      mutations.forEach(mutation => {
        mutation.addedNodes.forEach(node => {
          if (!(node instanceof HTMLElement)) return;
          // Translate the node itself if needed
          if (node.hasAttribute && node.hasAttribute('data-i18n')) {
            this.translateElement(node, lang);
          }
          // Translate any descendants with data-i18n
          const descendants = node.querySelectorAll ? node.querySelectorAll('[data-i18n]') : [];
          descendants.forEach(el => this.translateElement(el, lang));
        });
      });
    });
    observer.observe(document.body, { childList: true, subtree: true });
    this.domObserver = observer;
  }
  
  showLanguageSelection() {
    if (this.languageSelection) {
      this.languageSelection.hidden = false;
    }
    this.siteContent?.classList.add('is-blurred');
    document.body.classList.add('modal-open');
  }
  
  hideLanguageSelection() {
    if (this.languageSelection) {
      this.languageSelection.hidden = true;
    }
    this.siteContent?.classList.remove('is-blurred');
    document.body.classList.remove('modal-open');
  }
  
  // Dropdown control methods
  toggleDropdown() {
    console.log('Toggle dropdown called');
    if (this.languageDropdown.classList.contains('show')) {
      this.hideDropdown();
    } else {
      this.showDropdown();
    }
  }
  
  showDropdown() {
    this.languageDropdown.classList.remove('hidden');
    this.languageDropdown.classList.add('show');
  }
  
  hideDropdown() {
    this.languageDropdown.classList.remove('show');
    this.languageDropdown.classList.add('hidden');
  }
  
  selectLanguageFromDropdown(lang) {
    console.log('Language selected from dropdown:', lang);
    
    // Hide dropdown
    this.hideDropdown();
    
    // Update language immediately
    this.currentLanguage = lang;
    localStorage.setItem('selectedLanguage', lang);
    
    // Set language and update content
    this.setLanguage(lang);
    console.log('Language updated to:', lang);
  }
}

